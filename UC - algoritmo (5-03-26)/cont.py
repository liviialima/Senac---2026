dados = [10,20,30,60,30]
print(dados)
 
# número de elementos
print("Comprimento:", len(dados))

# conta ocorrências 
print("Quantas vezes o 30 aparece:",dados.cont(30))

# index - encontrar posição
print("Índice de 20:", dados.index(30))

# in verificar a existência 
print("20 está na lista?", 20 in dados)
print("100 está na lista?", 100 in dados)

